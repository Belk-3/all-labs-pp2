# here  we can see that each x y and z has a value with same sequence as a x,y,z.
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

#==============================================================================================================================

# also we can do like that
x = y = z = "Orange"
print(x)
print(y)
print(z)
# to get same value for all variables

#==============================================================================================================================

#or we can use it for list
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)
# each has a own variable and own value

